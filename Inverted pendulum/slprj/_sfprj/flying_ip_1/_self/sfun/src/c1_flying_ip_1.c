static real_T c1_mod(SFc1_flying_ip_1InstanceStruct *chartInstance, real_T c1_x,
                     real_T c1_y)
{
  real_T c1_r;
  real_T c1_xk;
  real_T c1_yk;
  real_T c1_b_x;
  real_T c1_b_y;
  real_T c1_c_x;
  real_T c1_d_x;
  real_T c1_e_x;
  real_T c1_f_x;
  real_T c1_g_x;
  real_T c1_h_x;
  real_T c1_i_x;
  real_T c1_c_y;
  real_T c1_j_x;
  real_T c1_d_y;
  real_T c1_k_x;
  real_T c1_l_x;
  c1_eml_scalar_eg(chartInstance);
  c1_xk = c1_x;
  c1_yk = c1_y;
  c1_b_x = c1_xk;
  c1_b_y = c1_yk;
  c1_eml_scalar_eg(chartInstance);
  if (c1_b_y == 0.0) {
    c1_r = c1_b_x;
  } else {
    c1_c_x = c1_b_y;
    c1_d_x = c1_c_x;
    c1_d_x = muDoubleScalarFloor(c1_d_x);
    if (c1_b_y == c1_d_x) {
      c1_e_x = c1_b_x / c1_b_y;
      c1_f_x = c1_e_x;
      c1_f_x = muDoubleScalarFloor(c1_f_x);
      c1_r = c1_b_x - c1_f_x * c1_b_y;
    } else {
      c1_r = c1_b_x / c1_b_y;
      c1_g_x = c1_r;
      c1_h_x = c1_g_x;
      c1_h_x = muDoubleScalarRound(c1_h_x);
      c1_i_x = c1_r - c1_h_x;
      c1_c_y = muDoubleScalarAbs(c1_i_x);
      c1_j_x = c1_r;
      c1_d_y = muDoubleScalarAbs(c1_j_x);
      if (c1_c_y <= 2.2204460492503131E-16 * c1_d_y) {
        c1_r = 0.0;
      } else {
        c1_k_x = c1_r;
        c1_l_x = c1_k_x;
        c1_l_x = muDoubleScalarFloor(c1_l_x);
        c1_r = (c1_r - c1_l_x) * c1_b_y;
      }
    }
  }

  return c1_r;
}

